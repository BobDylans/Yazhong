#!/usr/bin/env node
/**
 * Prebuild script: fetch products from admin API and save as JSON
 * Uses curl via child_process (Node's native fetch/https fails through proxy)
 */
import { execSync } from "child_process";
import fs from "fs";
import path from "path";
import { fileURLToPath } from "url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ADMIN_API = process.env.ADMIN_API_URL || "https://admin.rimhappywoods.top";
const API_URL = `${ADMIN_API}/api/products`;
const OUTPUT = path.resolve(__dirname, "..", "src/data/generated/products.json");
const CATE_OUT = path.resolve(__dirname, "..", "src/data/generated/categories.json");

function fetchJson(url) {
  const out = execSync(`curl -sf "${url}"`, {
    encoding: "utf-8",
    timeout: 15000,
    env: { ...process.env },
  });
  return JSON.parse(out);
}

async function main() {
  console.log("📦 Fetching products from admin API...");
  console.log(`   ${API_URL}`);

  const products = fetchJson(API_URL);

  const mapped = products.map((p) => {
    let image;
    if (p.image_key) {
      image = "/images/" + p.image_key;
    }
    return {
      id: p.id,
      title: p.title,
      description: p.description || undefined,
      image,
      badge: p.badge || undefined,
      badgeColor: p.badge_color || undefined,
      category: p.category_name || undefined,
      series: p.series_name || undefined,
    };
  }).filter(p => p.image);

  const cats = ["All", ...new Set(mapped.map((p) => p.category).filter(Boolean))];

  fs.mkdirSync(path.dirname(OUTPUT), { recursive: true });
  fs.writeFileSync(OUTPUT, JSON.stringify(mapped, null, 2));
  fs.writeFileSync(CATE_OUT, JSON.stringify(cats, null, 2));

  // Also write TypeScript file for client-side imports
  const tsOut = path.resolve(__dirname, "..", "src/data/generated/products-data.ts");
  const tsContent = `// Auto-generated by scripts/fetch-products.mjs — DO NOT EDIT
import type { ProductCardData } from "@/components/ProductCard";

export const generatedProducts: (ProductCardData & { series?: string })[] = ${JSON.stringify(mapped, null, 2)};

export const generatedCategories: string[] = ${JSON.stringify(cats, null, 2)};
`;
  fs.writeFileSync(tsOut, tsContent);

  console.log(`✅ ${mapped.length} products saved to src/data/generated/`);
}

main().catch((err) => {
  console.error("❌ Failed to fetch products:", err.message);
  process.exit(0);
});
